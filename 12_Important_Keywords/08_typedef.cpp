// In C++, the typedef keyword is used to create an alias (new name) for an existing data type. This can 
// improve code readability and make it easier to manage complex data types.

typedef unsigned int uint;

uint a = 5;  // same as: unsigned int a = 5;


/*
Modern Alternative: using (C++11 and above)

- using uint = unsigned int;

This is preferred in modern C++ for its cleaner syntax.
*/