// Abstraction = Implementation Hiding

/*
Advantages of Abstraction :
- Only you can make changes to your data or function, and no one else can.
- It makes the application secure by not allowing anyone else to see the background details.
- Increases the reusabilty of the code.
- Avoids duplication of your code 
*/



// Explain the difference between encapsulation and abstraction ?


/*

Feature	                        Encapsulation	                            Abstraction

Purpose	                        Hides data	                                Hides implementation
Achieved by	                    Access modifiers (private, public)	        Abstract classes, interfaces
Focus	                        Data protection and control	                Simplicity and usability
Example	                        private fields with get/set methods	        Function definitions without showing internals
Think of it like	            A capsule protecting data	                A remote control hiding circuit complexity

*/